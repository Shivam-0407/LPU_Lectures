package Intro_to_Strings;

public class Main {
    public static void main(String[] args) {
        // string is a sequence of 0 or more characters enclosed in double quotes
        String res = "Gangotri "+'c';

        System.out.println(res);

        // Arithmetic Operators -> +,-,/,*, % (it gives us the remainder)
        // Comparison Operators -> ==, !=,<=, >=

        System.out.println((3==2));
        System.out.println((3!=2));
        System.out.println((3<=2));
        System.out.println((3>=2));

        //Unary Operators -> +(no. is positive) , -(no. is negative) , ++ ,--
        int x = 90;
        System.out.println((x++));
        System.out.println((++x));
        int y =89;
        System.out.println((y--));
        System.out.println((--y));

        // conditional operator ->

        System.out.println("Hi everyone 😁✌️");


    }
}
